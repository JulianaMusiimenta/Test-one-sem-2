 // "===" vs "=="
 


//  The `==` operator compares two values for equality but **coerces** (automatically converts) the operands to the same type if they are not already the same type.
//  This means that `==` can return `true` even if the operands are of different types, 
// as long as they can be converted to the same type and have the same value after conversion.



// "===" vs "=="
//  The `===` operator compares two values for equality without performing any type conversion.
// this means that `===` checks both the value and the type of the operands.
//and if the operands are of different types, it will return `false`.
