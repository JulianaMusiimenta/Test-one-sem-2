//What are JavaScript objects, and how are they different from arrays? 

// Answer: 


// JavaScript objects are collections of key-value pairs, where keys are strings (or Symbols) and values can be of any type. 
// Arrays are a special type of object that represent ordered collections of values, and they have additional methods and properties for manipulating those values. 
// The main difference is that objects are unordered and accessed via keys, while arrays are ordered and accessed via indices.